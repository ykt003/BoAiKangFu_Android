public class MedicalHistoryRecord {
    /**
     * uuid
     */
    private String mhr_uuid;
    /**
     * 病例采集日期
     */
    private  String medical_history_date;
    /**
     * 病史陈述者
     */
    private String medical_history_relator;
    /**
     * 病例可靠性
     */
    private String medical_history_reliable;
    /**
     * 主诉
     */
    private String main_suit;
    /**
     * 病残史
     */
    private String disease_history;
    /**
     * 既往史
     */
    private String past_history;


    public String getMhr_uuid() {
        return mhr_uuid;
    }

    public void setMhr_uuid(String mhr_uuid) {
        this.mhr_uuid = mhr_uuid;
    }

    public String getMedical_history_date() {
        return medical_history_date;
    }

    public void setMedical_history_date(String medical_history_date) {
        this.medical_history_date = medical_history_date;
    }

    public String getMedical_history_relator() {
        return medical_history_relator;
    }

    public void setMedical_history_relator(String medical_history_relator) {
        this.medical_history_relator = medical_history_relator;
    }

    public String getMedical_history_reliable() {
        return medical_history_reliable;
    }

    public void setMedical_history_reliable(String medical_history_reliable) {
        this.medical_history_reliable = medical_history_reliable;
    }

    public String getMain_suit() {
        return main_suit;
    }

    public void setMain_suit(String main_suit) {
        this.main_suit = main_suit;
    }

    public String getDisease_history() {
        return disease_history;
    }

    public void setDisease_history(String disease_history) {
        this.disease_history = disease_history;
    }

    public String getPast_history() {
        return past_history;
    }

    public void setPast_history(String past_history) {
        this.past_history = past_history;
    }
}
